package aye2_practica7;

public class EmpleadoEj20 {
	private String nombre;
	private String apellido;
	private int nroemp;
	
	public EmpleadoEj20(String nombre, String apellido, int nroemp) {
		this.nombre = nombre;
		this.apellido = apellido;
		this.nroemp = nroemp;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getApellido() {
		return apellido;
	}

	public void setApellido(String apellido) {
		this.apellido = apellido;
	}

	public int getNroemp() {
		return nroemp;
	}

	public void setNroemp(int nroemp) {
		this.nroemp = nroemp;
	}
	
	
}
